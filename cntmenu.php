<h3>Bulldozer Wars</h3>
<p>
	<br>
<b>Task</b>
<br><br>
This will be a "one on one" game. Two Bots  will compete against each other. Each bot has to collect one ball at a time from the ball pit and throw this ball into the competitor’s godown (a fixed wooden pit). The competitor’s godown will be located diagonally opposite to the ball collecting pit of the boat and just beside the boundary line that divides the two bot's regions. The aim of each  bot should be to throw as many balls as it can into the opponent bot's godown (wooden pit). At the same time the bots are allowed to take out balls from their own godown and throw them back into the arena to decrease the number of balls in their own godown and thus reducing the negative marks that are awarded to the teams for number of balls in their own pit. The team with maximum points will be winner of that round. Each game will last for 5 minutes.
<br><br><br><b>Arena</b><br>
The arena (zones) will be such to test the navigation, steering and controlling capabilities of the bots. As well as it has a competitive task to perform. The bots have to throw balls in each others godown (wooden pit) located in the competitor’s region. The ball to be thrown will be small plastic balls of diameter 40 mm diameter. A maximum of 12 balls will be present in the ball containing pit of one side from where the team's bot will pick the balls, traverse the diagonal length and throw it into the competitor’s godown. The total size of arena will be 9400mm X 9400mm (l X b). This arena will be divided into two segments, one half for each of the two bots. So, space available for a single bot will be 9400mm *4700*mm (l X b). The godowns will be at a distance of 50 mm from mid line. 
<br>
<b><br><br>
Scoring</b>
<br>&raquo;Points will be awarded on the basis of number of balls thrown and number of balls in your own pit.
<br>&raquo;  Point system for awarding points to bots for throwing balls will be:
p= 10*b2 – 5*b1 ,Where, b2= number of balls in the godown of team fighting against your team 
,b1= number of balls in the godown of your own team
<br>&raquo;  The maximum number of points a team may obtain is: "120."
 <br>&raquo; There will be a penalty of 5 points everytime it crosses into your rivalry's region, i.e. crossing through the divison line into the opposite half of the arena. A bot will be considered to cross the divison line (mid-line in the arena), if its hull crosses the mid-line partially or completely and the boat’s hull is partly or completely in the rival’s zone.

